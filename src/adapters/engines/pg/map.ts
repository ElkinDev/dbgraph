/**
 * PostgreSQL schema extraction mappers.
 * Design §map.ts "rows → deterministic sorted RawCatalog".
 *
 * buildPgRawCatalog(input, scope) assembles a deterministic RawCatalog from
 * pre-fetched pg_catalog row arrays (testable with JSON fixtures — no live DB here).
 *
 * Determinism (ADR-008):
 *   - objects  : sorted by (KIND_RANK, schema, name) — mirrors mssql/map.ts
 *   - columns  : by attnum (ordinal)
 *   - constraints: by name (sorted alphabetically)
 *   - FK/index columns: by array ordinal (conkey / indkey position)
 *   - trigger events: grouped by trigger name, decoded from tgtype bitmask
 *   - schemas  : distinct-sorted from all extracted object schema fields
 *
 * Materialized views → { kind: 'view', extra: { materialized: true } }
 * NO new NodeKind introduced (US-028b).
 *
 * Edges from bodies via tokenizePgBody at confidence:'parsed' (no score).
 * supportsDependencyHints: false (body tokenizer is the SOLE edge source).
 *
 * US-028 (PostgreSQL adapter), US-028b (matview without model change),
 * US-007 (reads_from/writes_to classification), ADR-008 (determinism).
 */

